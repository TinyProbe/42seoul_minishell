#include "minish.h"

void	exit__(t_a *a)
{
	output(a, a->fd[STDOUT__], a->fd[STDERR__]);
	write(a->fd[STDOUT__], "exit\n", 5);
	endout(a, STDOUT__);
	exit(0);
}
