#include "ft.h"

size_t	ft_strcpy(t_i8 *dst, const t_i8 *src)
{
	size_t	idx;

	idx = 0;
	while (*src)
		dst[idx++] = *src++;
	dst[idx] = '\0';
	return (idx);
}
